declare(strict_types=1);

namespace Domain\Shop\Order\Enums;

use Filament\Support\Colors\Color;
use Filament\Support\Contracts\HasColor;
use Filament\Support\Contracts\HasIcon;
use Filament\Support\Contracts\HasLabel;
use Filament\Support\Icons\Heroicon;
use Illuminate\Support\Str;

enum OrderPaymentMethod: string implements HasColor, HasIcon, HasLabel
{
    case cash = 'cash';
    case g_cash = 'g_cash';

    public function getLabel(): string
    {
        return Str::headline($this->value);
    }

    public function getColor(): array
    {
        return match ($this) {
            self::cash => Color::Green,
            self::g_cash => Color::Orange,

        };
    }

    public function getIcon(): \BackedEnum
    {
        return match ($this) {
            self::cash => Heroicon::OutlinedBanknotes,
            self::g_cash => Heroicon::OutlinedCreditCard,
        };
    }
}
